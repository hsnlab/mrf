#!/usr/bin/env bash
set -x

SUT_USER=${SUTUSER:-`whoami`}
SUT_HOST=${SUTHOST:-"192.168.0.2"}
SUT_SSH_PORT=${SUTSSHPORT:-"22"}
VM_IP=${VMIP:-"192.168.122.74"}
MRF_ENABLE=${MRFENABLE:-"0"}
NFT_RULES_FILE=${NFTRULESFILE:-""}
SCRIPT=${SCRIPT:-"uname -a"}
NUM_CORES=${CORES:-"1"}
RX_FLOW_HASH=${RXFLOWHASH:-"sdfn"}

ssh $SUT_USER@$SUT_HOST -p $SUT_SSH_PORT "bash -s" <<EOF
export SUTUSER=$SUT_USER
export VMIP=$VM_IP
export MRFENABLE=$MRF_ENABLE
export NFTRULESFILE=$NFT_RULES_FILE
export CORES=$NUM_CORES
export RXFLOWHASH=$RX_FLOW_HASH
$SCRIPT
EOF
